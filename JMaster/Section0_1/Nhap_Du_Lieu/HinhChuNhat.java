package Section0_1;

public class HinhChuNhat {
	
	/**
	 * 
	 * @param x : chieu dai
	 * @param y : chieu rong
	 * @return
	 */
	public static double chuViHinhChuNhat(double x, double y) {
		return (x+y) *2;
		
	}
	
	public static double dienTichHinhChuNhat(double x, double y) {
		return x*y;
		
	}

}
