public class TinhToan {
	

	public static int Tong (int x, int y) { 

		 return x + y;
	 
	}
	
	public static double Hieu (double a, double b) { 
		
	
		return a - b;
	
	}
	
	public static float Tich( float c, float d) {
		
		return c*d;
		
	}
	
	public static long Thuong (long e, long f) {
		
		return e/f;
		
	}

}
