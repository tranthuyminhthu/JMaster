package Section0_1;

public class TamGiacVuong {
	
	/**
	 * 
	 * @param e : canh goc vuong
	 * @param f : canh goc vuong
	 * @param g : duong cao tam giac vuong
	 * @return
	 * 
	 */
	public static double chuViTamGiacVuong (double e, double f, double g) {
		return e + f + g;
		
	}
	
	public static double dienTichTamGiacVuong(double e, double f) {
		
		return 0.5*e*f;
		
	}
	      
}
